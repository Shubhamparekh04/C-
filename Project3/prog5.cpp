// Develop a program that prints the given Custom numeric pattern using a nested for loop.1 2 3 4 5 5 4 3 2 1

#include <iostream>
using namespace std;
int main()
{

    for (int i = 1; i <= 1; i++)
    {
        for (int j = i; j <= 5; j++)
        {
            cout << j << " ";
        }
        for (int k = 5; k >= 1; k--)
        {
            cout << k << " ";
        }
    }
    return 0;
}
